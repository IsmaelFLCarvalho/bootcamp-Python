#convertendo número inteiro em float

numero_inteiro = 20 #valor inteiro
numero_inteiro = float(numero_inteiro) #float para converter em float
print(numero_inteiro) #print com int convertido em inteiro

#convertendo número float para inteiro

numero_float = 10.4 #valor em float
numero_float = int(numero_float) #int para converter em inteiro
print(numero_float) #print com float convertido em inteiro

numero_para_string = 15.4
numero_para_string = str(numero_para_string)
print(numero_para_string)